function opt = add_results_options(opt)
  % (C) Copyright 2022 Remi Gau

  alpha = 0.001;
  minimum_cluster_size = 10;

  % Specify the result to compute
  opt.results(1) = default_output(opt);

  opt.results(1).nodeName = 'dataset_level';

  opt.results(1).name = {'all_olf', ...
                         'olf_lt_baseline', ...
                         'all_olfid', ...
                         'all_olfloc', ...
                         'left', ...
                         'right', ...
                         'eucalyptus', ...
                         'almond', ...
                         'Responses', ...
                         'olfloc_gt_olfid', ...
                         'olfid_gt_olfloc', ...
                         'olfloc-right_gt_left', ...
                         'olfloc-left_gt_right', ...
                         'olfid-right_gt_left', ...
                         'olfid-left_gt_right', ...
                         'olfloc-eucalyptus_gt_almond', ...
                         'olfloc-almond_gt_eucalyptus', ...
                         'olfid-eucalyptus_gt_almond', ...
                         'olfid-almond_gt_eucalyptus', ...
                         'eucalyptus_gt_almond', ...
                         'almond_gt_eucalyptus', ...
                         'left_gt_right', ...
                         'right_gt_left'};

  opt.results(1).MC =  'none';
  opt.results(1).p = alpha;
  opt.results(1).k = minimum_cluster_size;
  opt.results(1).csv = true;
  opt.results(1).atlas = 'AAL';

  opt.results(1).montage.background = fullfile(opt.dir.stats, ...
                                               'derivatives', ...
                                               'cpp_spm-groupStats', ...
                                               'space-MNI152NLin2009cAsym_desc-maskedMean_T1w.nii');

  opt.results(2) = opt.results(1);
  opt.results(2).nodeName = 'between_groups';

  opt.results(3) = opt.results(1);
  opt.results(3).nodeName = 'within_group';
end
